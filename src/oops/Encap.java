package oops;

public class Encap {
    private int age;
    private String name;
    private String dept;

    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {

        return age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }
}
