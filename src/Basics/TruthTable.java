package Basics;

public class TruthTable {
    public static void main(String[] args){

        int b =45;
        int a = 22;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a%=b);
        System.out.println(a=a-=b);
        System.out.println(a=a+=b);
        System.out.println(a=a*=b);
        System.out.println(a=a/=b);
        System.out.println(a=a%=b);





    }
}
