package Basics;

public class Unaryopp {
    public static void main(String[] args) {
        int a = 10;
        int b =58;
        System.out.println(++a);
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(--b);
        System.out.println(b--);
        System.out.println(b);
    }
}
